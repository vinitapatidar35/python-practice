# Escape Sequence in Python:
# Escape sequences are special characters used inside strings that start with a backslash (\)
# They help to represent characters that are hard to type directly or control formatting.

# Common escape sequences:
# \n  -> New line (moves text to next line)
# \t  -> Tab space (adds space like a tab key)
# \\  -> Backslash (\)
# \'  -> Single quote (')
# \"  -> Double quote (")
# \b -> backspace 

# Example:

print("Hello\n vinita")   # prints in two lines
print("Hello\nvinita") # no gap
print("Name:\tJohn")    # adds tab space
print("hello\bbyeeee")
print("path:\\vinita.com") # backslash always special char in python 

